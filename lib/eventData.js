// 환경에 따른 데이터 소스 선택
const USE_BACKEND =
  process.env.NODE_ENV === "production" ||
  process.env.NEXT_PUBLIC_USE_BACKEND === "true";

// eventId로 이벤트 데이터 조회 함수 (백엔드 연동)
export async function getEventById(eventId) {
  if (USE_BACKEND) {
    try {
      return await apiGetEventById(eventId);
    } catch (error) {
      console.error("백엔드에서 이벤트 데이터를 가져오는데 실패:", error);
      // 백엔드 실패 시 더미 데이터 폴백
      return getDummyEventById(eventId);
    }
  } else {
    // 개발 모드에서는 더미 데이터 사용
    return getDummyEventById(eventId);
  }
}

// 더미 데이터에서 이벤트 조회 (폴백용)
function getDummyEventById(eventId) {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      const event = DUMMY_EVENTS.find((event) => event.eventId === eventId);
      if (event) {
        resolve(event);
      } else {
        reject(new Error(`이벤트를 찾을 수 없습니다: ${eventId}`));
      }
    }, 100);
  });
}

